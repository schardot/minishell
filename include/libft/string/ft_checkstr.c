#include "../libft.h"

int	ft_checkstr(char *s, int (*f)(int))
{
	int	i;

	if (!s || !*s)
		return (1);
	i = 0;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i])
	{
		if (!f(s[i]))
			return (1);
		i ++;
	}
	return (0);
}
